class Command(BaseCommand):
    help = "Create About page and legal document pages in the Wagtail page tree"

    def handle(self, *args, **options):
        home = HomePage.objects.first()
        if not home:
            self.stderr.write("HomePage not found. Create it in Wagtail admin first.")
            return

        created = 0

        # About page
        if not Page.objects.filter(slug="about").exists():
            about = AboutPage(
                title="О компании",
                slug="about",
                intro="Авторские маршруты по Азии — небольшие группы, проверенные маршруты, настоящие места.",
            )
            home.add_child(instance=about)
            about.save_revision().publish()
            self.stdout.write(self.style.SUCCESS("Created: О компании (/about/)"))
            created += 1
        else:
            self.stdout.write("Skipped (exists): О компании")

        # Legal pages
        for data in LEGAL_PAGES:
            if Page.objects.filter(slug=data["slug"]).exists():
                self.stdout.write(f"Skipped (exists): {data['title']}")
                continue
            page = LegalPage(
                title=data["title"],
                slug=data["slug"],
                intro=data["intro"],
                content=data["content"],
            )
            home.add_child(instance=page)
            page.save_revision().publish()
            self.stdout.write(self.style.SUCCESS(f"Created: {data['title']} (/{data['slug']}/)"))
            created += 1

        self.stdout.write(self.style.SUCCESS(f"\nDone — {created} page(s) created."))

class Command(BaseCommand):
    help = "Заполняет БД страницами Wagtail: главная, каталог, 9 туров, контакты."

    def add_arguments(self, parser):
        parser.add_argument(
            "--reset",
            action="store_true",
            help="Удалить и пересоздать все страницы.",
        )

    def handle(self, *args, **options):
        from tours.models import (
            HomePage, CatalogPage, TourPage, TourDate, ContactPage,
        )

        if options["reset"]:
            self.stdout.write("Удаляю старые страницы…")
            HomePage.objects.all().delete()

        root = Page.objects.filter(depth=1).first()
        if not root:
            self.stderr.write("Root Page не найдена. Запустите: python manage.py migrate")
            return

        # ── HOME PAGE ────────────────────────────────────────
        existing_home = HomePage.objects.first()
        if existing_home:
            self.stdout.write(f"HomePage уже существует: {existing_home.title}")
            home = existing_home
        else:
            home = HomePage(
                title="Точка на карте",
                slug="home",
                hero_eyebrow="Авторские маршруты по Азии",
                hero_title="Замедлись, чтобы услышать Азию",
                hero_subtitle=(
                    "Небольшие группы. Настоящие маршруты. Каждый тур — "
                    "ручная работа куратора, который сам прошёл этот путь."
                ),
                hero_cta_text="Смотреть направления",
                body=[
                    ("manifest", {
                        "numeral": "01",
                        "eyebrow": "Философия пути",
                        "heading": "Путешествие — не маршрут. Это встреча с местом.",
                        "body": (
                            "<p>Мы не собираем людей в автобусы и не гонимся "
                            "за количеством достопримечательностей. Каждый тур собран "
                            "вручную — с местами, которые открываются только тогда, "
                            "когда вы не торопитесь.</p>"
                            "<p>Максимум 8 человек в группе. Нет программ по расписанию. "
                            "Есть пространство для случайных встреч и тишины.</p>"
                        ),
                        "stats": [
                            {"value": "8",  "label": "человек в группе"},
                            {"value": "0",  "label": "суеты в пути"},
                        ],
                    }),
                    ("quote", {
                        "quote":  "Я ехала за фотографиями, а нашла покой.",
                        "author": "Анна · маршрут Бали",
                    }),
                ],
            )
            root.add_child(instance=home)
            home.save_revision().publish()
            self.stdout.write(self.style.SUCCESS(f"OK HomePage создана: {home.title}"))

        # Обновляем Site
        Site.objects.update_or_create(
            hostname="localhost",
            defaults={
                "root_page":        home,
                "is_default_site":  True,
                "port":             80,
                "site_name":        "Точка на карте",
            },
        )

        # ── CATALOG PAGE ─────────────────────────────────────
        existing_catalog = CatalogPage.objects.first()
        if existing_catalog:
            catalog = existing_catalog
            self.stdout.write(f"CatalogPage уже существует: {catalog.title}")
        else:
            catalog = CatalogPage(
                title="Все туры",
                slug="catalog",
                intro="Авторские туры по Азии — каждый собран под небольшую группу и конкретную цель.",
            )
            home.add_child(instance=catalog)
            catalog.save_revision().publish()
            self.stdout.write(self.style.SUCCESS(f"OK CatalogPage создана: {catalog.title}"))

        # ── TOUR PAGES ───────────────────────────────────────
        for data in TOURS_DATA:
            if TourPage.objects.filter(slug=data["slug"]).exists():
                self.stdout.write(f"  • TourPage уже есть: {data['title']}")
                continue

            tour = TourPage(
                title=data["title"],
                slug=data["slug"],
                location=data["location"],
                summary=data["summary"],
                description=data["description"],
                duration=data["duration"],
                group_size=data["group_size"],
                group_size_max=data["group_size_max"],
                comfort=data["comfort"],
                difficulty=data["difficulty"],
                price_from=data["price_from"],
                country_tag=data["country_tag"],
                highlights=[("item", h) for h in data["highlights"]],
                itinerary=[
                    ("day", {
                        "day_number":  d["day_number"],
                        "title":       d["title"],
                        "description": d["description"],
                        "image":       None,
                    })
                    for d in data["itinerary"]
                ],
                accommodation=[
                    ("item", {
                        "name":        a["name"],
                        "type":        a["type"],
                        "description": a["description"],
                        "image":       None,
                    })
                    for a in data["accommodation"]
                ],
                included=[("item", i) for i in data["included"]],
                excluded=[("item", e) for e in data["excluded"]],
                cancel_policy=[
                    ("item", {
                        "period":          cp["period"],
                        "description":     cp["description"],
                        "refund_percent":  cp["refund_percent"],
                    })
                    for cp in data["cancel_policy"]
                ],
            )
            catalog.add_child(instance=tour)
            tour.save_revision().publish()

            # Tags
            if data["tags"]:
                tour.tags.add(*data["tags"])

            # Dates
            for d in data["dates"]:
                start = datetime.date.fromisoformat(d["start"])
                end   = datetime.date.fromisoformat(d["end"])
                TourDate.objects.create(
                    page=tour,
                    start_date=start,
                    end_date=end,
                    price=d["price"],
                    total_spots=d["total"],
                    spots_left=d["left"],
                )

            self.stdout.write(self.style.SUCCESS(f"  OK Тур создан: {data['title']}"))

        # ── CONTACT PAGE ─────────────────────────────────────
        existing_contact = ContactPage.objects.first()
        if existing_contact:
            self.stdout.write(f"ContactPage уже существует: {existing_contact.title}")
        else:
            contact = ContactPage(
                title="Контакты",
                slug="contact",
                intro="<p>Расскажите нам о своей мечте — мы найдём для неё маршрут.</p>",
                email="hello@tochka.travel",
                telegram="https://t.me/tochka_travel",
                whatsapp="https://wa.me/79001234567",
                address="Тбилиси, ул. Бетлеми, 14",
                hours="Пн–Пт · 11:00–19:00",
            )
            home.add_child(instance=contact)
            contact.save_revision().publish()
            self.stdout.write(self.style.SUCCESS(f"OK ContactPage создана: {contact.title}"))

        self.stdout.write(self.style.SUCCESS("\nГотово! Сайт заполнен данными."))
        self.stdout.write("Запустите: python manage.py createsuperuser")
        self.stdout.write("Затем:     python manage.py runserver")
